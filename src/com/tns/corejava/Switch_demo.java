package com.tns.corejava;

public class Switch_demo {
	public static void main(String[] args) {
		int x=1;
		switch(x)
		{
		case 0:
			System.out.println(0);
			break;
		case 1:
		System.out.println(1);
	//	break;
		case 2:
			System.out.println(2);
			//break;
			default:
				System.out.println("default statement");

	}

	}
}
